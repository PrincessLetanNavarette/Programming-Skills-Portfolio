#This if block will be executed when the player inputs the correct answer, which makes the if condition true.
alien_color = 'red'
if alien_color == 'red':
    print("You just earned 5 points!")
else:
    print("You just earned 10 points!")
    
#This else block will be executed when the player inputs a different answer, which makes the if condition false; therefore, the else block would be an output.
alien_color = 'green'
if alien_color == 'red':
    print("You just earned 5 points!")
else:
    print("You just earned 10 points!")